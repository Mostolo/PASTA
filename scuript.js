function FuncSportello() {
    var uno = document.getElementById("Sportello")
    var due = document.getElementById("Cubi")
    var tre = document.getElementById("Cerchi")
    var quattro = document.getElementById("Emoticon")
    var cinque = document.getElementById("Cupido")
    var sei = document.getElementById("Cuori")

    {
    due.style.display = "none"
    tre.style.display = "none"
    quattro.style.display = "none"
    cinque.style.display = "none"
    sei.style.display = "none"
    }

    if (uno.style.display === "block") {
        uno.style.display = "none"
    } else {
        uno.style.display = "block"
    }
}

